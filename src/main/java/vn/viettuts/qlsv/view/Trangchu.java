    public Doanvien getNhanvienInfo() {
        if (!validateTen() || !validateID() || !validateNgaysinh()
                || !validateGioitinh() || !validateDiachi()
                || !validateChibo() || !validateNvd() || !validateDangphi()) {
            return null;
        }
        try {
            Doanvien nhanvien = new Doanvien();
            nhanvien.setId(stt_txt.getText().trim());
            nhanvien.setName(ten_txt.getText().trim());
            nhanvien.setAge(ngaysinh_txt.getText().trim());
            nhanvien.setAddress(quequan_txt.getText().trim());
            nhanvien.setSex(gioitinh_txt.getText().trim());
            // nhanvien.setSdt(sdtjTextField.getText().trim());
            nhanvien.setChibo(chibo_txt.getText().trim());
            // nhanvien.setLuong(Double.parseDouble(luongjTextField.getText().trim()));
            nhanvien.setDangphi(dangphi_txt.getText().trim());
            nhanvien.setNvd(nvd_txt.getText().trim());
            return nhanvien;
        } catch (Exception e) {
            showMessage(e.getMessage());
        }
        return null;
    }

