     public String checkkhenthuong(Doanvien doanvien){
        String tmp;
        
        if(doanvien.getGpa()>=9 && doanvien.getDrl()>=9){
            tmp = "Xuất sắc";
        }
        else if(doanvien.getGpa()>=8 && doanvien.getDrl()>=8){
            tmp = "Giỏi";
        }
        else if(doanvien.getGpa()>=7 && doanvien.getDrl()>=7){
            tmp = "Khá";
        }
        else if(doanvien.getGpa()>=5 && doanvien.getDrl()>=5){
            tmp = "Trung Bình";
        }
        else if(doanvien.getGpa() < 5 || doanvien.getDrl() < 5) {
            tmp = "Yếu";
        }
        else tmp ="Không đủ điều kiện xếp loại";
        return tmp;
    }
    
    
